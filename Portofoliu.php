    <center><b><font size="4" >"Armonie si Sanatate"</font></b></center><br>

 Filiala de Cruce Rosie Constanta impreuna cu: Cabinet de Psihoterapie si Dezvoltare Personala "OM", Centrul Medical Provita 2000 si Asociatia Studentilor si Medicilor Voluntari desfasoara pe parcursul a 8 luni proiectul "Armonie si Sanatate"
<br><br>
<b>De ce acest proiect?</b> Desi in ultimii 30 de ani medicii au recunoscut gradual efectul stresului psihologic si noi credem ca stresul se refera la efectul negativ pe care il au emotiile asupra corpului nostru. Realitatea este insa sensibil mai complexa.Exista multe si diferite tipuri de stres, nesesizabile la prima vedere, dar care sunt potentiale si de cele mai multe ori, efective amenintari ale sanatatii.
<br><br>
Starile de veselie, implinire, bucurie pentru viata, recunostinta sunt <b>"un medicament" natural</b> care imbunatateste starea generala de sanatate. Pe de alta parte emotiile negative precum ura, mania, gelozia, frica si altele de acest tip au un efect <b>devastator</b> asupra sanatatii. Consideram ca normalitate echilibrul dintre diferite organe si sisteme ale corpului, dintre corp si emotii, precum si dintre corp si mediul inconjurator. Daca pentru un motiv oarecare echilibrul uneia dintre componente se schimba atunci sunt conditii de aparitie a unor boli: cardiovasculare, respiratorii, endocrine, digestive, renale, etc.
<br><br>
Prin acest proiect Filiala de Cruce Rosie Constanta, Cabinet de Psihoterapie si Dezvoltare Personala "OM", Centrul Medical Provita 2000 si Asociatia Studentilor si Medicilor Voluntari incearca constientizarea persoanelor participante la proiect ca mentinerea sanatatii necesita o buna capacitate de a face fata stresului.
<br><br>
        "Armonie si Sanatate" se adreseaza atata copiilor cat si adultilor.
