 Delincventa juvenila reflecta o inadaptare la sistemul juridic si moral al societatii, fiind cea mai importantă dintre deviantele negative, care includ:
 <ul>
 <li> violarea si încălcarea normelor de convieţuire socială</li>
 <li> integritatea persoanei</li>
 <li> drepturile şi libertăţile individului</li>
 <li> afectând întreaga comunitate</li>
 </ul>

Comportamentul deviant are la origine o serie de factori, printre care se numără:
<ul>
<li> influenţa negativă a anturajului </li>
<li> informarea insuficientă cu privire la pedepsirea faptelor antisociale</li>
</ul>

Astfel, scopul campaniei <b>"Sunt responsabil!"</b> vizeaza educarea antiinfractionala a tinerilor in vederea evitarii implicarii acestora in comiterea de fapte antisociale.
<br><br>
Furtul reprezinta o infractiune comisa adesea de catre tineri, inclusiv minori, integrati în anturaje dubioase care isi ocupa timpul implicandu-se în activitati infractionale.
<br><br>
Unul dintre obiectivele specifice ale campaniei consta in informarea, pe parcursul a 3 luni, a unui numar de 2000 de tineri cu vârste cuprinse intre 14 şi 18 ani, din 5 localitati din mediul rural, de pe raza judetului Constanta, cu privire la consecintele implicarii in comiterea infractiunii de furt, sub influenta anturajului.
<br><br>
Cel de-al doilea obiectiv al proiectului vizeaza informarea, pe parcursul a 3 luni, a unui numar de 2000 de tineri cu varste cuprinse între 14 şi 18 ani, din 5 localitati din mediul rural de pe raza judetului Constanta, cu privire la alternativele pozitive de petrecere a timpului liber în detrimentul activitatilor infractionale.
<br><br>
Proiectul se va implementa în 5 localitati din mediul rural, selectate în functie de situatia statistica înregistrata la nivelul Inspectoratului de Politie Judetean Constanta, activitatile fiind desfasurate de caătre tinerii voluntari ai Asociatiei Studentilor si Medicilor Voluntari si politistii desemnati.
