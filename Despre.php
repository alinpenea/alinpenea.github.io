    <center><b><u><font size="5">Ce este ASMV?</font></u></b></center><br>

<b>Asociatia Studentilor si Medicilor Voluntari</b> este o organizatie non-profit infiintata in 2007, la initiativa unor tineri medici si studenti <br>care si-au propus dezvoltarea pe plan local a unui grup informativ, care sa promoveze sanatatea printre tineri si sa ajute la perfectionarea<br> in domeniul pregatirii studentilor la medicina.<br><br><br><br><br>

<center><b><u><font size="5">Misiune</font></u></b></center><br>
Educarea, informarea si dezvoltarea in domeniul medical a comunitatii, precum si acordarea de ajutor moral si material persoanelor suferinde.<br><br><br><br><br>

<center><b><u><font size="5">Obiective</font></u></b></center><br>
<b>1.</b> Educaţie medicală în randul tinerilor şi perfectionarea în domeniul pregătirii profesionale a studenţilor.<br>
<b>2.</b> Acordarea de ajutoare materiale, de asistenţă şi sprijin moral persoanelor suferinde.<br>
<b>3.</b> Organizarea de manifestări ştiinţifice naţionale şi internationale, simpozioane, conferinţe, campanii informative şi alte evenimente<br> pe teme medicale.<br>
<b>4.</b> Tiparirea de publicaţii şi materiale informative.<br>
<b>5.</b> Realizarea de proiecte ce vizeaza problemele de mediu.<br>
<b>6.</b> Colaborarea cu institutii medicale şi alte organizatii din ţara şi străinatate in vederea unor schimburi de experienţă şi a realizării<br> scopurilor de mai sus.<br><br><br><br><br>

<center><b><u><font size="5">Principii generale</font></u></b></center><br>
ASMV se adreseaza atat studentilor si cadrelor medicale, incercand sa faciliteze acumularea de cunostinte si experienta, cat si tinerilor din afara<br> ariei medicale, prin campanii de informare si preventie. Alaturi de proiectele ce vizeaza sanatatea publica, ASMV sprijina actiunile de<br> protectie a mediului si acorda suport moral si material celor in suferinta.
<br><br>
ASMV promoveaza competenta si performanta. Prin actiunile organizate, ASMV doreste sa creasca calitatea educatiei medicale<br> romanesti, in special a pregatirii individuale a studentului la medicina.<br><br><br>


