<br><br>
Mentionam faptul ca initiativa organizatorilor acestui proiect reprezinta prima actiune de acest gen derulata vreodata in satele si comunele din judetul Constanta, selectate in scopul implementarii proiectului.
<br><br>
Scenariul piesei de teatru va fi conceput de catre tinerii implicati direct in punerea ei in scena si vor fi sustinuti de catre tinerii organizatori ai proiectului si un actor profesionist. Actorii care vor juca in piesa de teatru si vor scrie scenariul vor fi recrutati din randul tinerilor din cadrul Centrelor de plasament sau tineri rromi, dar si alti tineri, dornici sa participe la realizarea acestui proiect. Astfel, vor fi stimulate creativitatea si spiritul de initiativa a acestor tineri, care vor depune un efort comun in scopul atingerii obiectivelor propuse in cadrul proiectului.
<br><br>
Un alt element inovativ al acestui proiect consta in combinarea non-formalului cu formalul, intrucat intreaga activitate va fi condusa de catre tinerii care au initiat proiectul si cei atrasi in proiect, insa vor fi sustinuti de  reprezentanti ai Politiei si alte autoritati locale care vor aduce un plus de credibilitate si siguranta, datorita prestantei institutiilor pe care le reprezinta.
