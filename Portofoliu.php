 Proiectul vizeaza combaterea violentei impotriva femeilor din mediul rural prin informarea acestora, a membrilor familiilor lor si a intregii comunitati cu privire la drepturile pe care le au ca oameni si ca cetateni ai acestei tari, prevederi legale de natura sa protejeze femeile abuzate si sa pedepseasca agresorii, precum si institutii care le pot oferi ajutor specializat.
